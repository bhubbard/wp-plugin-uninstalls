-- WordPress Plugin Uninstall SQL Script

-- Delete Options & Transients
DELETE FROM wp_options WHERE option_name IN ('REVI_WIDGETS', 'REVI_API_METHOD', 'REVI_API_WORKING_URL', 'REVI_API_KEY', 'REVI_LAST_UPGRADE_VERSION', 'REVI_SELECTED_LANGUAGE', 'REVI_SUBSCRIPTION', 'REVI_SECURITY_KEY', 'REVI_LANG', 'REVI_ACTIVE_LANGUAGES', 'REVI_MODULE_VERSION', 'REVI_ORDER_STATUSES', 'REVI_SYNC_PRODUCT_DESCRIPTION', 'REVI_DISPLAY_WIDGET_QUESTIONS_BIG', 'REVI_DISPLAY_WIDGET_QUESTIONS_SMALL', 'REVI_DISPLAY_WIDGET_WITHOUT_REVIEWS', 'REVI_CRON_TOKEN', 'REVI_NOTIFICATIONS', 'REVI_PRODUCT_METADATA', 'REVI_TAB_REVIEWS', 'REVI_PRODUCT_STARS_POSITION', 'REVI_DISPLAY_WIDGET_FLOATING', 'REVI_WOOCOMMERCE_REVIEWS', 'REVI_DISPLAY_PRODUCT_LIST_ALIGN', 'REVI_DISPLAY_PRODUCT_LIST_EMPTY', 'REVI_DISPLAY_PRODUCT_LIST_BLANK_SPACE', 'REVI_DISPLAY_PRODUCT_LIST_TEXT');
DELETE FROM wp_options WHERE option_name LIKE 'REVI_WIDGET_%';

-- Delete Metadata
DELETE FROM wp_postmeta WHERE meta_key IN ('wpml_language');
DELETE FROM wp_usermeta WHERE meta_key IN ('wpml_language');
DELETE FROM wp_termmeta WHERE meta_key IN ('wpml_language');
DELETE FROM wp_commentmeta WHERE meta_key IN ('wpml_language');

