<?php

// If uninstall not called from WordPress, then exit.
if ( ! defined( 'WP_UNINSTALL_PLUGIN' ) ) {
	exit;
}

// Delete Options
delete_option('tags_page_version');
delete_site_option('tags_page_version');
delete_option('tags_page_options');
