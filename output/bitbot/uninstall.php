// Delete Options
delete_option('bitbot_api_key');
delete_site_option('bitbot_api_key');
global $wpdb;
$options = $wpdb->get_col( $wpdb->prepare( "SELECT option_name FROM {$wpdb->options} WHERE option_name LIKE %s", 'bitbot_%' ) );
foreach ( $options as $opt ) {
	delete_option( $opt );
	delete_site_option( $opt );
}
delete_option('bitbot_enabled');
delete_site_option('bitbot_enabled');
delete_option('bitbot_widget_position');
delete_site_option('bitbot_widget_position');
delete_option('bitbot_primary_color');
