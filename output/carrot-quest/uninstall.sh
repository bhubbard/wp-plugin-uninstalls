#!/bin/bash
# WP-CLI Uninstall Script

# Delete Options
wp option delete 'carrotquest_api_key'
wp option delete 'carrotquest_api_secret'
wp option delete 'carrotquest_auth_key'
wp option delete 'carrotquest_auth'

# Delete Transients
wp db query "DELETE FROM wp_options WHERE option_name LIKE '_transient_carrotquest_%' OR option_name LIKE '_site_transient_carrotquest_%'"

