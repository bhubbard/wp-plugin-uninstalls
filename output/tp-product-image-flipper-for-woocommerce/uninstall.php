<?php

// If uninstall not called from WordPress, then exit.
if ( ! defined( 'WP_UNINSTALL_PLUGIN' ) ) {
	exit;
}

// Delete Options
delete_option('remove_duplicate_images');
delete_site_option('remove_duplicate_images');
delete_option('images_from_gallery_only');
delete_site_option('images_from_gallery_only');
delete_option('add_product_link_to_image');
