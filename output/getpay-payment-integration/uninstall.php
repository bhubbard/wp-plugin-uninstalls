// Delete Options
delete_option('woocommerce_getpay_settings');
delete_site_option('woocommerce_getpay_settings');

// Delete Transients
global $wpdb;
$transients = $wpdb->get_col( $wpdb->prepare( "SELECT option_name FROM {$wpdb->options} WHERE option_name LIKE %s OR option_name LIKE %s", '_transient_getpay_field_errors_%', '_site_transient_getpay_field_errors_%' ) );
foreach ( $transients as $transient ) {
	delete_option( $transient );
}
