<?php

// If uninstall not called from WordPress, then exit.
if ( ! defined( 'WP_UNINSTALL_PLUGIN' ) ) {
	exit;
}

// Delete Options
delete_option('dresoo_plugin_options');
delete_site_option('dresoo_plugin_options');
delete_option('outfits_db_version');
